// Icon lookup helper
export const getIcon = (name: string, size?: number, className?: string) => {
  const icons: Record<string, React.FC<IconProps>> = {
    home: HomeIcon,
    calendar: CalendarIcon,
    target: TargetIcon,
    check: CheckIcon,
    sparkle: SparkleIcon,
    clock: ClockIcon,
    mapPin: MapPinIcon,
    fileText: FileTextIcon,
    lightbulb: LightbulbIcon,
    plus: PlusIcon,
    x: XIcon,
    edit: EditIcon,
    trash: TrashIcon,
    chevronLeft: ChevronLeftIcon,
    chevronRight: ChevronRightIcon,
    menu: MenuIcon,
    send: SendIcon,
    checkCircle: CheckCircleIcon,
    xCircle: XCircleIcon,
    user: UserIcon,
    logout: LogoutIcon,
    sun: SunIcon,
    moon: MoonIcon,
    cloud: CloudIcon,
    cloudRain: CloudRainIcon,
    thermometer: ThermometerIcon,
    users: UsersIcon,
  };

  const IconComponent = icons[name];
  if (!IconComponent) return null;
  return <IconComponent size={size} className={className} />;
};

